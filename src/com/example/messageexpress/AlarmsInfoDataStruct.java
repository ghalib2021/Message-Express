package com.example.messageexpress;

public class AlarmsInfoDataStruct {
	private int hour;
	private int minute;
	private int month;
	private int year;
	private int day;
	private int id;
	private String repeatoption;
	public int getHour() {
		return hour;
	}
	public void setHour(int hour) {
		this.hour = hour;
	}
	public int getMinute() {
		return minute;
	}
	public void setMinute(int minute) {
		this.minute = minute;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getRepeatoption() {
		return repeatoption;
	}
	public void setRepeatoption(String repeatoption) {
		this.repeatoption = repeatoption;
	}
	
}
